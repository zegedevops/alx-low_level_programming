#include <stdio.h>
/**
* add - add two integrets
* @a: first int 
* @b: second int
* Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract two integrets
* @a: first int 
* @b: second int
* Return: subtracted result
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* div - divide two integrets
* @a: first int 
* @b: second int
* Return: division result
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - mod two integrets
* @a: first int 
* @b: second int
* Return: final result
*/
int mod(int a, int b)
{
	return (a % b);
}

/**
* mul - multiply two integrets
* @a: first int 
* @b: second int
* Return: multiply
*/
int mul(int a, int b)
{
	return (a * b);
}
